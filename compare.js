//Compare operator
//1.Equal Operator
console.log(2 == 2); //true
console.log(2 == '2'); //true

//2.Not equal Operator
console.log(2 != 2); //false
console.log('hello' != 'Hello'); //true

//3.Stric equal Operator
console.log(2 === 2); //true
console.log(2 === '2'); //false

//3.Stric not equal Operator
console.log(2 !== '2'); //true
console.log(2 !== 2); //false

